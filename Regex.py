def Get_Config_Path():
    try:
        with open("config/config.json", "r", encoding="utf-8") as Paths:
            CONFIG = json.load(Paths)
            return CONFIG
    except FileNotFoundError:
        print("File not Found: config/config.json")
        return None

    except json.JSONDecodeError:
        print(f"Error Decoding JSON file.")
        return None


